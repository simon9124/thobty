#7-1.创建《用户》表
CREATE TABLE thobty_users(
	uid INT PRIMARY KEY AUTO_INCREMENT,		#自增ID
	uname VARCHAR(32),										#用户名
	upwd VARCHAR(32),											#用户密码
	email VARCHAR(128),										#用户邮箱
	token VARCHAR(64),										#账号激活码
	token_exptime INT(10),								#激活码有效期
	status TINYINT(1),										#状态 0-未激活 1-已激活
	regtime DATE,													#注册时间
	ulevel  VARCHAR(32)										#当前用户级别
);

