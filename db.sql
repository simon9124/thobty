#5-1.创建《图片》表
CREATE TABLE thobty_pic(
	pid INT PRIMARY KEY AUTO_INCREMENT,		#自增ID
	title VARCHAR(32),										#图片标题
	detail1 VARCHAR(64),									#图片详情1
	detail2 VARCHAR(64),									#图片详情2
	detail3 VARCHAR(64),									#图片详情3
	detail4 VARCHAR(64),									#图片详情4
	detail5 VARCHAR(64),									#图片详情5
	class	VARCHAR(32),										#图片隶属分类
	style	VARCHAR(32),										#图片隶属风格
	color	VARCHAR(32),										#图片隶属颜色
	#每张图片，在网站将显示2种尺寸 -> 430*430 & 950*950
	md VARCHAR(128),											#中图片路径
	lg VARCHAR(128),											#大图片路径
	viewed INT,														#浏览次数
	downloaded INT,												#下载次数
	collected INT,												#收藏次数
	#预留4列以便网站更新，为图片增加列选项(如:单张图/图片作者/购买次数...)
	ext1 VARCHAR(64),
	ext2 VARCHAR(64),
	ext3 VARCHAR(64),
	ext4 VARCHAR(64)
);

